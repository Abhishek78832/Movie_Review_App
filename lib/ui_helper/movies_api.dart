class Movies_api {
  String? id;
  String? type;
  String? primaryTitle;
  String? originalTitle;
  PrimaryImage? primaryImage;
  int? startYear;
  int? runtimeSeconds;
  List<String>? genres;
  Rating? rating;
  String? plot;

  Movies_api({
    this.id,
    this.type,
    this.primaryTitle,
    this.originalTitle,
    this.primaryImage,
    this.startYear,
    this.runtimeSeconds,
    this.genres,
    this.rating,
    this.plot,
  });

  Movies_api.fromJson(Map<String, dynamic> json) {
    id = json['id'];
    type = json['type'];
    primaryTitle = json['primaryTitle'];
    originalTitle = json['originalTitle'];
    primaryImage = json['primaryImage'] != null
        ? new PrimaryImage.fromJson(json['primaryImage'])
        : null;
    startYear = json['startYear'];
    runtimeSeconds = json['runtimeSeconds'];
    genres = json['genres'].cast<String>();
    rating = json['rating'] != null
        ? new Rating.fromJson(json['rating'])
        : null;
    plot = json['plot'];
  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = new Map<String, dynamic>();
    data['id'] = this.id;
    data['type'] = this.type;
    data['primaryTitle'] = this.primaryTitle;
    data['originalTitle'] = this.originalTitle;
    if (this.primaryImage != null) {
      data['primaryImage'] = this.primaryImage!.toJson();
    }
    data['startYear'] = this.startYear;
    data['runtimeSeconds'] = this.runtimeSeconds;
    data['genres'] = this.genres;
    if (this.rating != null) {
      data['rating'] = this.rating!.toJson();
    }
    data['plot'] = this.plot;
    return data;
  }
}

